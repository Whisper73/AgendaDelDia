    private void listarTodo() {
        if (this.controlador.TieneContenido()) {
            List<Tarea> tareas = this.controlador.MostarTareas();
            modelo.removeAllElements();
            for (Tarea t : tareas) {
                modelo.addElement(t.toString());
            }
        } else {

            JOptionPane.showMessageDialog(null, "No hay tareas para mostrar");
        }
    }
