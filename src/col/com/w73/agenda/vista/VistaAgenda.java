    private void btnActualizarTareaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarTareaMouseClicked

        if (!this.outListaTareas.isSelectionEmpty()) {

            String tareaSelec = this.outListaTareas.getSelectedValue();
            String[] atributosTareaSelec = tareaSelec.split(",");
            Tarea tareaSel = new Tarea(atributosTareaSelec[0], atributosTareaSelec[1]);
            int indexTareaSelec = this.controlador.BuscarTarea(tareaSel);
            this.controlador.ActualizarTarea(IngresarNuevaTarea(), indexTareaSelec);
            modelo.removeAllElements();
            this.listarTodo();
        }

    }//GEN-LAST:event_btnActualizarTareaMouseClicked
    private Tarea IngresarNuevaTarea() {
        String[] horas = new String[24 * 4];
        String hora, descripcion;
        for (int i = 0, k = 0; i < horas.length && k < 24; i += 4, k++) {
            for (int j = 0, p = 0; j <= 45 && p < 4; j += 15, p++) {

                hora = (k + ":" + j);
                horas[i + p] = hora;
            }
        }
        Object obj;

        do {

            obj = JOptionPane.showInputDialog(null, "Ingrese la Hora", "Hora", JOptionPane.WARNING_MESSAGE, null, horas, 0);

        } while (obj == null);

        hora = obj.toString();

        do {

            descripcion = JOptionPane.showInputDialog(null, "Ingrese la tarea para realiar a las " + hora + " horas : ");

        } while (descripcion.equals("") || descripcion.contains(","));

        Tarea tareaNueva = new Tarea(hora, descripcion);
        return tareaNueva;

    }

